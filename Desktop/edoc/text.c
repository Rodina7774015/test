#include <stdio.h>

int main() {
    char fname[256];  // Буфер для имени файла (до 255 символов)
    FILE *in;         // Указатель на файл

    printf("Укажите файл для чтения: ");
    if (scanf("%255s", fname) != 1) {  // Читаем имя файла (макс. 255 символов)
        printf("Ошибка ввода имени файла!\n");
        return 1;
    }

    in = fopen(fname, "rt");  // Открываем файл в режиме чтения текста
    if (in == NULL) {
        printf("Ошибка открытия файла '%s'!\n", fname);
        return 1;
    }

    // Чтение файла посимвольно
    int ch;  // int, а не char, потому что fgetc возвращает int (для EOF)
    while ((ch = fgetc(in)) != EOF) {  // Читаем символ, пока не конец файла
        putchar(ch);  // Выводим символ на экран (можно заменить на обработку)
    }

    fclose(in);  // Закрываем файл
    return 0;
}